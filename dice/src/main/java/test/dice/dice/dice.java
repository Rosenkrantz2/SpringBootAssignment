package test.dice.dice;

public class dice {
	
	Integer id;
	Integer count;
	
	public dice() {
		
	}


	public dice(Integer id, Integer count) {
		super();
		this.id = id;
		this.count = count;
	}



	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public Integer getCount() {
		return count;
	}

	public void setCount(Integer count) {
		this.count = count;
	}
	

	
	

}
